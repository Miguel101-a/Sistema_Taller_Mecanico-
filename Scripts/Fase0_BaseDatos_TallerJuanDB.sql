-- ---- LOGIN ----
CREATE PROCEDURE sp_Login
    @Usuario    VARCHAR(50),
    @HashClave  VARCHAR(64)
AS
BEGIN
    SET NOCOUNT ON;
    SELECT E.CEDULA, E.NOMBRE, E.USUARIO, E.CARGO, E.ID_ROL, R.NOMBRE AS ROL_NOMBRE
    FROM EMPLEADO E
    INNER JOIN ROL R ON E.ID_ROL = R.ID_ROL
    WHERE E.USUARIO = @Usuario
      AND E.CONTRASENA = @HashClave
      AND E.ESTADO = 'ACTIVO';
END
GO

-- ---- PERMISOS POR ROL ----
CREATE PROCEDURE sp_PermisosPorRol
    @IdRol INT
AS
BEGIN
    SET NOCOUNT ON;
    SELECT P.ID_PERMISO, P.CLAVE, P.DESCRIPCION, P.MODULO
    FROM ROL_PERMISO RP
    INNER JOIN PERMISO P ON RP.ID_PERMISO = P.ID_PERMISO
    WHERE RP.ID_ROL = @IdRol;
END
GO

CREATE PROCEDURE sp_TodosLosPermisos
AS
BEGIN
    SET NOCOUNT ON;
    SELECT ID_PERMISO, CLAVE, DESCRIPCION, MODULO FROM PERMISO ORDER BY MODULO, CLAVE;
END
GO

-- Asignar un permiso a un rol (idempotente)
CREATE PROCEDURE sp_AsignarPermiso
    @IdRol INT, @IdPermiso INT
AS
BEGIN
    SET NOCOUNT ON;
    IF NOT EXISTS (SELECT 1 FROM ROL_PERMISO WHERE ID_ROL=@IdRol AND ID_PERMISO=@IdPermiso)
        INSERT INTO ROL_PERMISO (ID_ROL, ID_PERMISO) VALUES (@IdRol, @IdPermiso);
END
GO

-- Quitar un permiso a un rol
CREATE PROCEDURE sp_QuitarPermiso
    @IdRol INT, @IdPermiso INT
AS
BEGIN
    SET NOCOUNT ON;
    DELETE FROM ROL_PERMISO WHERE ID_ROL=@IdRol AND ID_PERMISO=@IdPermiso;
END
GO

CREATE PROCEDURE sp_ListarRoles
AS
BEGIN
    SET NOCOUNT ON;
    SELECT ID_ROL, NOMBRE, ESTADO FROM ROL WHERE ESTADO='ACTIVO' ORDER BY NOMBRE;
END
GO


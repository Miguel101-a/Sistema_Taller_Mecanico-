        PRECIO_COMPRA=@PrecioCompra, PRECIO_VENTA=@PrecioVenta WHERE CODIGO=@Codigo;
END
GO

CREATE PROCEDURE sp_Producto_Eliminar
    @Codigo VARCHAR(20)
AS
BEGIN
    SET NOCOUNT ON;
    UPDATE PRODUCTO SET ESTADO='INACTIVO' WHERE CODIGO=@Codigo;
END
GO

CREATE PROCEDURE sp_Producto_AlertasStockMinimo
AS
BEGIN
    SET NOCOUNT ON;
    SELECT CODIGO, NOMBRE, STOCK_ACTUAL, STOCK_MINIMO
    FROM PRODUCTO WHERE ESTADO='ACTIVO' AND STOCK_ACTUAL <= STOCK_MINIMO;
END
GO

